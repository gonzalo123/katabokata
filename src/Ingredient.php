<?php

class Ingredient implements \IngredientIface
{
    private $name;
    private $price;

    public function __construct()
    {
    }

    public function setName($name)
    {
        $this->name = $name;
    }

    public function setPrice($price)
    {
        $this->price = $price;
    }

    public function getName()
    {
        return $this->name;
    }

    public function getPrice()
    {
        return $this->price;
    }
}